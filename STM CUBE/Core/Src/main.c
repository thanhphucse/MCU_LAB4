typedef struct{
	// Pointer to the task (must be a ’ void ( void ) ’ function )
	void(*pTask)(void);
	// Delay (ticks) until the function will (next) be run
	uint32_t Delay;
	// Interval (ticks) between subsequent runs .
	uint32_t Period;
	// Incremented (by scheduler) when task i s due to execute
	uint8_t RunMe;
	//This i s a hin t to solve the question below .
	uint32_t TaskID ;
} sTask ;

	// MUST BE ADJUSTED FOR EACH NEW PROJECT
#define SCH_MAX_TASKS 1
#define NO_TASK_ID 0
sTask SCH_tasks_G[SCH_MAX_TASKS];
unsigned char current_index_task = 0;
unsigned char current_count_task = 0;


unsigned char SCH_Delete_Task(const unsigned char TASK_INDEX){
	unsigned char Return_code ;
	SCH_tasks_G [TASK_INDEX].pTask = 0x0000 ;
	SCH_tasks_G [TASK_INDEX].Delay = 0;
	SCH_tasks_G [TASK_INDEX].Period = 0;
	SCH_tasks_G [TASK_INDEX].RunMe = 0;
	current_index_task --;
	current_count_task --;
	return Return_code ; // return status
}
void SCH_Init(void){
	unsigned char i ;
	for(i = 0; i < SCH_MAX_TASKS; i++){
		SCH_Delete_Task(i);
	}
}
void SCH_Add_Task ( void (*pFunction)() , uint32_t DELAY, uint32_t PERIOD){
//	unsigned char current_index_task = 0;
	if(current_index_task < SCH_MAX_TASKS){
		current_count_task ++;
		SCH_tasks_G[current_index_task].pTask = pFunction;
		SCH_tasks_G[current_index_task].Delay = DELAY;
		SCH_tasks_G[current_index_task].Period =  PERIOD;
		SCH_tasks_G[current_index_task].RunMe = 0;

		SCH_tasks_G[current_index_task].TaskID = current_index_task;


		current_index_task++;
	}
}

void SCH_Update(void){
	for(int i = 0; i < current_count_task; i++){
		if (SCH_tasks_G[i].Delay > 0){
			SCH_tasks_G[i].Delay --;
		}else{
			SCH_tasks_G[i].Delay = SCH_tasks_G[i].Period;
			SCH_tasks_G[i].RunMe += 1;
		}
	}
}

void SCH_Dispatch_Tasks(void){
	for(int i = 0; i < current_count_task; i++){
		if(SCH_tasks_G[i].RunMe > 0){
			SCH_tasks_G[i].RunMe--;
			(*SCH_tasks_G[i].pTask)();
		}
	}
}

